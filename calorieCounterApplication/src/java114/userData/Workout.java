package java114.userData;

public class Workout {

    private String date;
    private String type;
    private String time;
    private String heartRate;
    private String caloriesBurned;



    public Workout() {

    }


    /**

     * Returns the value of date.

     */

    public String getDate() {

        return date;

    }





    /**

     * Sets the value of date.

     * @param date The value to assign date.

     */

    public void setDate(String date) {

        this.date = date;

    }





    /**

     * Returns the value of type.

     */

    public String getType() {

        return type;

    }





    /**

     * Sets the value of type.

     * @param type The value to assign type.

     */

    public void setType(String type) {

        this.type = type;

    }





    /**

     * Returns the value of time.

     */

    public String getTime() {

        return time;

    }





    /**

     * Sets the value of time.

     * @param time The value to assign time.

     */

    public void setTime(String time) {

        this.time = time;

    }





    /**

     * Returns the value of heartRate.

     */

    public String getHeartRate() {

        return heartRate;

    }





    /**

     * Sets the value of heartRate.

     * @param heartRate The value to assign heartRate.

     */

    public void setHeartRate(String heartRate) {

        this.heartRate = heartRate;

    }


    public String getCaloriesBurned() {

        return caloriesBurned;

    }

    public void setCaloriesBurned(String caloriesBurned) {

        this.caloriesBurned = caloriesBurned;
    }





}